// ── Shared renderer ───────────────────────────────────

function _renderLegal({ title, lead, date, sections, footer }) {
  const app = document.getElementById('app');

  app.innerHTML = `
    <div class="legal-screen">
      <div class="legal-topbar">
        <button class="btn btn--icon btn--ghost legal-back-btn js-back">${_iconBack()}</button>
        <span class="legal-topbar__title">Re Food Calculator</span>
      </div>

      <div class="legal-wrap">
        <section class="legal-hero">
          <h1 class="legal-hero__title">${title}</h1>
          <p class="legal-hero__lead">${lead}</p>
          <div class="legal-hero__meta">Последнее обновление: ${date}</div>
        </section>

        ${sections.map(s => `
          <section class="legal-card">
            <h2 class="legal-card__title">${s.title}</h2>
            <div class="legal-card__body">${s.content}</div>
          </section>
        `).join('')}

        <div class="legal-footer">${footer}</div>
      </div>
    </div>
  `;

  app.querySelector('.js-back').addEventListener('click', () => navigateBack());
}

function _iconBack() {
  return `<svg width="20" height="20" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round">
    <polyline points="15 18 9 12 15 6"/>
  </svg>`;
}
